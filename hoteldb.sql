--  DDL for Procedure CONFIRM_ONLINE_BOOKING
--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "HOTEL_ADMIN"."CONFIRM_ONLINE_BOOKING" (
    p_booking_id IN NUMBER,
    p_room_number IN VARCHAR2,
    p_entered_otp IN VARCHAR2,
    p_hotel_name IN VARCHAR2,
    p_success OUT NUMBER,
    p_message OUT VARCHAR2
) AS
    v_booking_rec hms_online_bookings%ROWTYPE;
    v_room_exists NUMBER;
    v_room_occupied NUMBER;
BEGIN
    p_success := 0; -- Default to failure

    -- 1. Find the online booking and verify OTP
    BEGIN
        SELECT * INTO v_booking_rec
        FROM hms_online_bookings
        WHERE booking_id = p_booking_id
          AND hotel_name = p_hotel_name
          AND status = 'PENDING';
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            p_message := 'Booking ID not found or already confirmed.';
            RETURN;
    END;

    IF v_booking_rec.otp != p_entered_otp THEN
        p_message := 'Invalid OTP provided.';
        RETURN;
    END IF;

    -- 2. Check if the assigned room exists for the hotel
    SELECT COUNT(*) INTO v_room_exists
    FROM hms_rooms
    WHERE room_number = p_room_number AND hotel_name = p_hotel_name;

    IF v_room_exists = 0 THEN
        p_message := 'Assigned room number does not exist.';
        RETURN;
    END IF;

    -- 3. Check if the assigned room is already occupied
    SELECT COUNT(*) INTO v_room_occupied
    FROM hms_guests
    WHERE room_number = p_room_number AND hotel_name = p_hotel_name;

    IF v_room_occupied > 0 THEN
        p_message := 'Assigned room is already occupied.';
        RETURN;
    END IF;

    -- 4. All checks passed, perform the check-in
    INSERT INTO hms_guests (guest_name, mobile_number, room_number, check_in_time, hotel_name)
    VALUES (v_booking_rec.guest_name, v_booking_rec.mobile_number, p_room_number, CURRENT_TIMESTAMP, p_hotel_name);

    -- 5. Update the online booking status to CONFIRMED
    UPDATE hms_online_bookings
    SET status = 'CONFIRMED', room_number_assigned = p_room_number
    WHERE booking_id = p_booking_id;

    COMMIT;
    p_success := 1;
    p_message := 'Booking confirmed and guest checked in successfully!';

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        p_message := 'An unexpected error occurred: ' || SQLERRM;
        p_success := 0;
END;

